package bean;

public class Subject {
	//変数宣言
	private String cd;
	private String name;
	private School school;

	//ゲッター、セッター
	public String getCd() {
		return cd;
	}
	public void setCd(String cd) {
		this.cd = cd;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public School getSchool() {
		return school;
	}
	public void setSchool(School school) {
		this.school = school;
	}



}
